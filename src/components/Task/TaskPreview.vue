<template>
	<div
		class="task-preview"
		:class="{ 'is-done': taskItem.status.done }"
	>
		<UIButton
			class="task-preview__button-done button--icon"
			@button-click="changeTaskDone(taskItem)"
		>
			<span class="button__icon">
				<i class="icon">
					<FontAwesomeIcon icon="check-circle" />
				</i>
			</span>
		</UIButton>
		<div class="task-preview__name">
			<p>
				{{ taskItem.name }}
			</p>
		</div>
		<ul class="task-preview__tools">
			<li>
				<UIButton class="task-preview__button-edit button--icon">
					<span class="button__icon">
						<i class="icon">
							<FontAwesomeIcon icon="edit" />
						</i>
					</span>
				</UIButton>
			</li>
			<li>
				<UIButton
					class="task-preview__button-remove button--icon"
					@button-click="removeTask(taskItem.id)"
				>
					<span class="button__icon">
						<i class="icon">
							<FontAwesomeIcon icon="trash" />
						</i>
					</span>
				</UIButton>
			</li>
		</ul>
	</div>
</template>

<script>
	import {mapActions} from 'vuex';
	import UIButton from '@/components/UI/UIButton.vue';

	export default {
		name: 'TaskPreview',
		components: {
			UIButton
		},
		props: {
			taskItem: {
				type: Object,
				required: true
			}
		},
		methods: {
			...mapActions([
				'removeTask',
				'updateTask'
			]),
			changeTaskDone(task) {
				task.status.done = !task.status.done;
				this.updateTask(task);
			}
		}
	};
</script>

